//  Write a f(x) that returns an array with all the numbers from 1 to 255 and the sum;

function arrayNumSum() {
    var sum = 0;
    for (var x = 0; x <= 255; x++) {
        sum += x;
        return ('New number:', x, 'Sum:', sum);
        // console.log('New number:', x, 'Sum:', sum);
    }
}

