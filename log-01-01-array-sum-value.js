//  Write a function that returns an array with all the numbers from 1 to 255 and the sum.

function printSum1255(){
    var sum = 0;
    for (var num = 0; num <= 255; num++){
        sum += num;
        console.log(`New number:`, num, `Sum:`, sum);
    }
}

